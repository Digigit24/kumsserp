// ============================================================================
// CLASS SESSION INTERFACE
// ============================================================================

export interface ClassSession {
  id: string;
  subject: string;
  class: string;
  class_id: number;
  section: string;
  section_id: number;
  time: string;
  room: string;
  date: string;
  teacher_id: string;
  teacher_name: string;
  total_students: number;
  attendance_taken: boolean;
  attendance_percentage?: number;
  present_count?: number;
  absent_count?: number;
}

// ============================================================================
// STUDENT FOR ATTENDANCE INTERFACE
// ============================================================================

export interface StudentForAttendance {
  id: number;
  roll_number: string;
  name: string;
  class_id: number;
  class_name: string;
  section_id: number;
  section_name: string;
  profile_image?: string;
  attendance_status?: 'present' | 'absent' | 'late' | 'excused' | 'half_day' | null;
  remarks?: string;
}

// ============================================================================
// ATTENDANCE SESSION INTERFACE (for marking)
// ============================================================================

export interface AttendanceSession {
  id: string;
  class_session: ClassSession;
  students: StudentForAttendance[];
  marked_at?: string;
  marked_by?: string;
  qr_code?: string;
  qr_code_expiry?: string;
}

