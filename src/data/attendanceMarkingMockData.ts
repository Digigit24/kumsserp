// Helper function to get students by class session
export const getStudentsByClassSession = (sessionId: string): StudentForAttendance[] => {
  const session = mockClassSessions.find((s) => s.id === sessionId);
  if (!session) return [];

  if (session.class_id === 101) return [...mockStudentsClass10A];
  if (session.class_id === 111) return [...mockStudentsClass11B];
  if (session.class_id === 121) return [...mockStudentsClass12A];

  return [];
};

// Helper function to create an attendance session
export const createAttendanceSession = (sessionId: string): AttendanceSession | null => {
  const classSession = mockClassSessions.find((s) => s.id === sessionId);
  if (!classSession) return null;

  return {
    id: `attendance-${sessionId}`,
    class_session: classSession,
    students: getStudentsByClassSession(sessionId),
    qr_code: `QR-${sessionId}-${Date.now()}`,
    qr_code_expiry: new Date(Date.now() + 10 * 60 * 1000).toISOString(), // 10 minutes
  };
};

